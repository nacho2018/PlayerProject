package com.mvn.test.myproject;

/**
 * The Class Player.
 */
public class Player {
	
	/** The name. */
	private String name;
	
	/** The game. */
	private String game;
	
	/** The age. */
	private int age;
	
	/**
	 * Gets the name.
	 *
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	
	/**
	 * Sets the name.
	 *
	 * @param name the new name
	 */
	public void setName(String name) {
		this.name = name;
	}
	
	/**
	 * Gets the game.
	 *
	 * @return the game
	 */
	public String getGame() {
		return game;
	}
	
	/**
	 * Sets the game.
	 *
	 * @param game the new game
	 */
	public void setGame(String game) {
		this.game = game;
	}
	
	/**
	 * Gets the age.
	 *
	 * @return the age
	 */
	public int getAge() {
		return age;
	}
	
	/**
	 * Sets the age.
	 *
	 * @param age the new age
	 */
	public void setAge(int age) {
		this.age = age;
	}
	

}
